public class gorev5_1 {

	public static void main(String[] args) {
		int a = 1;
		int akare = a*a;
		int akup = a*a*a;
		System.out.println("1'in karesi = " + akare);
		System.out.println("1'in küpü = " + akup);
		System.out.println("");
		
		int b = 2;
		int bkare = b*b;
		int bkup = b*b*b;
		System.out.println("2'nin karesi = " + bkare);
		System.out.println("2'nin küpü = " + bkup);
		System.out.println("");
		
		int c = 3;
		int ckare = c*c;
		int ckup = c*c*c;
		System.out.println("3'ün karesi = " + ckare);
		System.out.println("3'ün küpü = " + ckup);
		System.out.println("");
		
		int d = 4;
		int dkare = d*d;
		int dkup = d*d*d;
		System.out.println("4'ün karesi = " + dkare);
		System.out.println("4'ün küpü = " + dkup);
		System.out.println("");
		
		int e = 5;
		int ekare = e*e;
		int ekup = e*e*e;
		System.out.println("5'in karesi = " + ekare);
		System.out.println("5'in küpü = " + ekup);
	}

}

